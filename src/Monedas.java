public class Monedas {
     private int pesoArgentino;
   private int realBrasileno;
   private int dolarEstadounidense;

    public int getPesoArgentino() {
        return pesoArgentino;
    }

    public void setPesoArgentino(int pesoArgentino) {
        this.pesoArgentino = pesoArgentino;
    }

    public int getRealBrasileno() {
        return realBrasileno;
    }

    public void setRealBrasileno(int realBrasileno) {
        this.realBrasileno = realBrasileno;
    }

    public int getDolarEstadounidense() {
        return dolarEstadounidense;
    }

    public void setDolarEstadounidense(int dolarEstadounidense) {
        this.dolarEstadounidense = dolarEstadounidense;
    }
}
